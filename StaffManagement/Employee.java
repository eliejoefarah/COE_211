package StaffManagement;
public class Employee {
	public String first;
	public String last;
	public int age;
	public double salary;


	public Employee(String first, String last, int age, double salary) {
		this.first=first;
		this.last=last;
		this.age=age;
		this.salary=salary;
		
	}
	
	
	

}
